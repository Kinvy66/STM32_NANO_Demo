/**
 * @brief
 * @param GPIO_Pin
 */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
    HAL_Delay(10);      	//Ïû¶¶
    switch(GPIO_Pin)
    {
        case KEY1_Pin:
            if(KEY1==0)
            {
               LED1_TOGGLE();
               LED2_TOGGLE();
            }
            break;
        case KEY2_Pin:
            if(KEY2==0)
            {
                LED1_TOGGLE();
            }
            break;
        case KEY3_Pin:
            if(KEY3==0)
            {
                LED2_TOGGLE();
            }
            break;
        case WK_UP_Pin:
            if(WK_UP==1)
            {
                LED1_TOGGLE();
                LED2_TOGGLE();
            }
    }
}
